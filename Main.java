/* Exemplo de Atribuição e Pré-Incremento */
public class Main {

    public static void main(String[] args) {
        int x = 0, y = 0;  // Inicializando as variáveis

        System.out.println(x = 10);  // Atribui 10 a x e exibe 10

        System.out.println(y = ++x);  // Incrementa x e atribui o valor a y, depois exibe y
    }
}
